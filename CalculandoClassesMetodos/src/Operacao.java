
public class Operacao {
	
	public void exibirSoma(double v1, double v2 ) {
		double result;
		result = (v1 + v2);
		System.out.println("A soma foi  " +result);
		
	}
	public void exibirDiv(double v1, double v2) {
		double result;
		result = v1 / v2;
		System.out.println("A divisao foi  " +result);
		
	}
	public void exibirSub(double v1, double v2 ) {
		double result;
		result = v1 - v2;
		System.out.println("A subtraçao foi  " +result);
		
	}
	public void exibirMult(double v1, double v2) {
		double result;
		result = v1 * v2;
		System.out.println("A multiplicaçao foi  " +result);
		
	}

}
